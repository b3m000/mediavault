export function getContentById(contentId: string): MediaContent | undefined {
  return mockLibrary.find((item) => item.id === contentId);
}

export function getMediaItemByIds(contentId: string, itemId: string): MediaItem | undefined {
  const content = getContentById(contentId);
  return content?.items?.find((item) => item.id === itemId);
}

